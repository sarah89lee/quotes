import UIKit

extension UIFont {
    
    class func extraBoldFont(size: CGFloat) -> UIFont {
        return UIFont(name: "QanelasSoftDEMO-ExtraBold", size: size)!
    }
    
    class func mediumFont(size: CGFloat) -> UIFont {
        return UIFont(name: "QanelasSoftDEMO-Medium", size: size)!
    }
    
    class func lightFont(size: CGFloat) -> UIFont {
        return UIFont(name: "QanelasSoftDEMO-UltraLight", size: size)!
    }
    
}
